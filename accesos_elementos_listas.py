lista = [10, 20, 30, 40, 50]

primer_elemento = lista [0]
print('Primer elemento: ', primer_elemento)

ultimo_elemento = lista [-1]
print('Último elemento: ', ultimo_elemento)

elemento_medio = lista [2]
print('Elemento en medio: ', elemento_medio)

primeros_tres = lista[0:3]
print('Primeros 3 elementos: ', primeros_tres)

desde_indice_1 = lista[1:]
print('Elementos desde el índice 1 hasta el final', desde_indice_1)

lista_completa = lista[:]
print('Lista completa: ', lista_completa)

